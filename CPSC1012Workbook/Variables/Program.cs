// Variables Example
// Task - Display a greeting to a user including their name and age.

// Declared two variables
int userAge;
string userName;

// Use the assignment operator (=) to assign a value to variable
userAge = 21;
userName = "Jane Doe";

Console.WriteLine(userAge); // 21

// Assign multiple times (overwite)
userAge = 31; // userAge will be 31

Console.WriteLine(userAge); // 31
Console.WriteLine(userName);