// Data Type Examples

// Numeric: Integer, Floating Point
// Integer -> int
Console.WriteLine(100); // int 100

// Floating Point -> double
Console.WriteLine(100.1); // double 100.1

// Character: Character, String
// Character -> char
Console.WriteLine('A'); // char 'A'

// String -> string
Console.WriteLine("ABC"); // string "ABC"
// Strings can contain 0, 1, or many characters
Console.WriteLine(""); // empty string
Console.WriteLine("A"); // single character string
Console.WriteLine("   "); // string with whitespace only